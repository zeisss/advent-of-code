package main

import (
	"fmt"
	"strings"
)

type Grid struct {
	Data [][]int
}

func ParseGrid(input string) Grid {
	g := Grid{}

	lines := strings.Split(input, "\n")
	for _, line := range lines {
		row := make([]int, len(line))
		for i, char := range line {
			if char == '@' {
				row[i] = 1
			}
		}
		g.Data = append(g.Data, row)
	}

	return g
}

func (g Grid) Copy() Grid {
	newGrid := Grid{}
	for _, row := range g.Data {
		newRow := make([]int, len(row))
		copy(newRow, row)
		newGrid.Data = append(newGrid.Data, newRow)
	}
	return newGrid
}

func (g Grid) RemoveSurroundedRolls() (Grid, int64) {
	newGrid := g.Copy()
	const threshold = 4
	var rolls int64
	for y := 0; y < len(g.Data); y++ {
		for x := 0; x < len(g.Data[y]); x++ {
			if g.HasRoll(x, y) == 0 {
				continue
			}
			neighbors := g.HasRoll(x-1, y-1) + g.HasRoll(x, y-1) + g.HasRoll(x+1, y-1) + // above
				g.HasRoll(x-1, y) + 0 + g.HasRoll(x+1, y) + // same row
				g.HasRoll(x-1, y+1) + g.HasRoll(x, y+1) + g.HasRoll(x+1, y+1)
			if neighbors < threshold { // below
				// fmt.Println(x, y)
				rolls++
				newGrid.Data[y][x] = 0 // Remove roll
			}
		}
	}
	return newGrid, rolls
}

func (g Grid) HasRoll(x, y int) int {
	if y < 0 || y >= len(g.Data) {
		return 0
	}
	if x < 0 || x >= len(g.Data[y]) {
		return 0
	}
	return g.Data[y][x]
}

func Part1(input string) int64 {
	_, n := ParseGrid(input).RemoveSurroundedRolls()
	return n
}

func Part2(input string) int64 {
	grid := ParseGrid(input)
	var n int64
	var totalRemoved int64
	for {
		grid, n = grid.RemoveSurroundedRolls()
		totalRemoved += n
		if n == 0 {
			break
		}
	}
	return totalRemoved
}

